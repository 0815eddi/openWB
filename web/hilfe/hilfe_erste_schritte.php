	<h3>Bedienung</h3>
		<p> Lademodus</p><br>
			Der Unterschied zwischen "Standby" und "Stop" ist, dass bei "Stop" keine Ladung erlaubt ist.<br>
		Im Standby  Modus kann die Ladung durch Nachtladen und Zielladen aktiviert werden<br>
